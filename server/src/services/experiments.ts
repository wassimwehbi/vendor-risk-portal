// A/B experimentation engine (spec 0015): deterministic variant assignment,
// exposure/conversion telemetry, and per-variant results with significance.
//
// Experiment DEFINITIONS are config-as-code (experiments/*.yml), compiled to
// server/src/data/experiments.json by scripts/experiments.mjs and loaded here at
// boot. Assignment is a pure function of (experiment key, user id) — sticky and
// storage-free. Every app route is authenticated, so the subject is always user.id.
import { createHash } from 'node:crypto';
import { readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { db, nowIso } from '../db';
import type { AccessScope } from '../routes/_helpers';
import type { Experiment, ExperimentResults, FlagAssignments, VariantComparison, VariantResult } from '../types';

const __dirname = dirname(fileURLToPath(import.meta.url));
// Generated by `scripts/experiments.mjs build`. Overridable for tests (cf. VRP_DB_PATH).
const REGISTRY_PATH = process.env.VRP_EXPERIMENTS_PATH || join(__dirname, '..', 'data', 'experiments.json');

// Optional env, read once at load. Absent values disable the relevant endpoint.
export const experimentsConfig = {
  portalOrigin: process.env.EXPERIMENTS_PORTAL_ORIGIN || '',
  readToken: process.env.EXPERIMENTS_READ_TOKEN || '',
  ghClientId: process.env.GH_OAUTH_CLIENT_ID || '',
};

let cache: Experiment[] | null = null;

/**
 * Shape guard mirroring the build-time contract (scripts/experiments.mjs): a malformed entry is
 * dropped rather than trusted. Enforces the same variant invariants the validator does — ≥2
 * variants, non-negative weights, and weights summing to 100 — so assignVariant's cumulative-weight
 * walk can never silently over-/under-assign on a registry that bypassed the build.
 */
function isValidExperiment(e: unknown): e is Experiment {
  const x = e as Partial<Experiment>;
  if (!x || typeof x.key !== 'string' || typeof x.name !== 'string' || typeof x.status !== 'string') return false;
  if (!Array.isArray(x.variants) || x.variants.length < 2) return false;
  if (!x.variants.every((v) => v && typeof v.key === 'string' && typeof v.weight === 'number' && v.weight >= 0)) {
    return false;
  }
  return x.variants.reduce((sum, v) => sum + v.weight, 0) === 100;
}

/**
 * Loads the experiment registry. A missing/invalid file — or any malformed entry — is dropped,
 * yielding a clean registry rather than ever breaking the app. A missing file is WARNED (it usually
 * means the build step didn't run — see the Dockerfile stage / `predev`) so an empty registry from a
 * missing artifact is diagnosable, not silently indistinguishable from "no experiments defined".
 */
export function getRegistry(): Experiment[] {
  if (cache) return cache;
  try {
    const parsed = JSON.parse(readFileSync(REGISTRY_PATH, 'utf8')) as { experiments?: unknown };
    cache = Array.isArray(parsed.experiments) ? parsed.experiments.filter(isValidExperiment) : [];
  } catch (err) {
    const detail = (err as NodeJS.ErrnoException).code ?? (err as Error).message;
    console.warn(`[experiments] no usable registry at ${REGISTRY_PATH} (${detail}); serving 0 experiments.`);
    cache = [];
  }
  return cache;
}

/** Test seam: drop the cached registry so the next read re-loads from disk. */
export function _resetRegistryCache(): void {
  cache = null;
}

export function getExperiment(key: string): Experiment | undefined {
  return getRegistry().find((e) => e.key === key);
}

/** Every metric name (primary + secondary) an experiment measures. */
function metricsOf(exp: Experiment): string[] {
  return exp.metrics ? [exp.metrics.primary, ...(exp.metrics.secondary ?? [])] : [];
}

// ---- Deterministic assignment (pure) ---------------------------------------

/** Stable 0..99 bucket for (experiment, subject). Same inputs → same bucket forever, no storage. */
export function bucket(key: string, subjectId: string): number {
  return createHash('sha256').update(`${key}:${subjectId}`).digest().readUInt32BE(0) % 100;
}

/** A running experiment is live only within its [start, end] window (inclusive, UTC calendar dates). */
export function withinWindow(exp: Experiment, today = new Date().toISOString().slice(0, 10)): boolean {
  if (exp.start && today < exp.start) return false;
  if (exp.end && today > exp.end) return false;
  return true;
}

/** Whether the scope's user falls inside the experiment's targeting (an omitted/empty facet = everyone). */
export function eligible(exp: Experiment, scope: AccessScope): boolean {
  // NB: keep this in sync with the validator's `audiencesOverlap` (scripts/experiments.mjs) —
  // both encode "omitted or empty roles/tenants = everyone".
  const t = exp.targeting;
  if (!t) return true;
  if (t.roles && t.roles.length > 0 && !t.roles.includes(scope.effectiveRole)) return false;
  if (t.tenants && t.tenants.length > 0) {
    if (scope.activeTenantId == null || !t.tenants.includes(scope.activeTenantId)) return false;
  }
  return true;
}

/** The variant this user gets, or null when the experiment is not live or the user is not targeted. */
export function assignVariant(exp: Experiment, scope: AccessScope): string | null {
  if (exp.status !== 'running' || !withinWindow(exp)) return null;
  if (!eligible(exp, scope)) return null;
  const b = bucket(exp.key, String(scope.userId));
  let cumulative = 0;
  for (const v of exp.variants) {
    cumulative += v.weight;
    if (b < cumulative) return v.key;
  }
  return exp.variants[0]?.key ?? null; // unreachable for valid (sum===100) entries; defensive only
}

/** Evaluate every live + eligible experiment for this user. */
export function evaluateAll(scope: AccessScope): FlagAssignments {
  const flags: FlagAssignments = {};
  for (const exp of getRegistry()) {
    const variant = assignVariant(exp, scope);
    if (variant) flags[exp.key] = variant;
  }
  return flags;
}

// ---- Telemetry -------------------------------------------------------------

/** Idempotently record that a user was exposed to a variant (first render wins, PK on exp_key+user). */
export function recordExposure(key: string, variant: string, scope: AccessScope): void {
  db.prepare(
    `INSERT OR IGNORE INTO experiment_exposures (exp_key, variant, user_id, tenant_id, first_seen)
     VALUES (?, ?, ?, ?, ?)`,
  ).run(key, variant, scope.userId, scope.activeTenantId, nowIso());
}

/**
 * Record a metric event, attributed ONLY to experiments the user was actually EXPOSED to that
 * declare this metric. Binding each event to specific experiment keys eliminates cross-experiment
 * contamination when experiments share a metric name, and discards undeclared/garbage metrics (so
 * the table can't be grown unbounded). Returns how many experiments the event was attributed to.
 *
 * Metrics are still client-fired, so a user could fire a declared metric without performing the
 * underlying action. Closing that fully needs server-authoritative emission (calling this from the
 * action's own route) — tracked as a spec 0015 follow-up.
 */
export function recordEvent(metric: string, scope: AccessScope): number {
  const exposures = db
    .prepare('SELECT exp_key FROM experiment_exposures WHERE user_id = ?')
    .all(scope.userId) as Array<{ exp_key: string }>;
  if (exposures.length === 0) return 0;
  const declaring = new Set(
    getRegistry()
      .filter((e) => metricsOf(e).includes(metric))
      .map((e) => e.key),
  );
  if (declaring.size === 0) return 0;

  const insert = db.prepare(
    'INSERT INTO experiment_events (exp_key, metric, user_id, tenant_id, ts) VALUES (?, ?, ?, ?, ?)',
  );
  const ts = nowIso();
  let recorded = 0;
  for (const { exp_key } of exposures) {
    if (!declaring.has(exp_key)) continue;
    insert.run(exp_key, metric, scope.userId, scope.activeTenantId, ts);
    recorded++;
  }
  return recorded;
}

// ---- Results + significance ------------------------------------------------

/** Per-variant exposed/converted/rate plus a z-test of each variant vs the control. */
export function computeResults(exp: Experiment): ExperimentResults {
  const metric = exp.metrics?.primary ?? null;

  // Single pass: exposures LEFT JOINed to their own (exp_key-bound) conversion events. Attribution
  // is user-level — matching user-level bucketing — so a user who converts under a different active
  // tenant than they were exposed in is still counted; tenant_id is stored for analytics only.
  const rows = db
    .prepare(
      `SELECT e.variant AS variant,
              COUNT(DISTINCT e.user_id) AS exposed,
              COUNT(DISTINCT CASE WHEN ev.user_id IS NOT NULL THEN e.user_id END) AS converted
         FROM experiment_exposures e
         LEFT JOIN experiment_events ev
           ON ev.exp_key = e.exp_key AND ev.user_id = e.user_id AND ev.metric = ? AND ev.ts >= e.first_seen
        WHERE e.exp_key = ?
        GROUP BY e.variant`,
    )
    .all(metric, exp.key) as Array<{ variant: string; exposed: number; converted: number }>;
  const byVariant = new Map(rows.map((r) => [r.variant, r]));

  const variants: VariantResult[] = exp.variants.map((v) => {
    const r = byVariant.get(v.key);
    const exposed = r?.exposed ?? 0;
    const converted = r?.converted ?? 0;
    return { key: v.key, exposed, converted, rate: exposed > 0 ? converted / exposed : 0 };
  });

  const control = variants[0];
  const comparisons: VariantComparison[] = control
    ? variants.slice(1).map((v) => ({ variant: v.key, control: control.key, ...twoProportionTest(control, v) }))
    : [];

  return { key: exp.key, name: exp.name, status: exp.status, metric, variants, comparisons };
}

/** Two-proportion z-test (two-sided) + 95% CI for the rate difference (variant − control). */
function twoProportionTest(
  control: VariantResult,
  variant: VariantResult,
): Pick<VariantComparison, 'z' | 'pValue' | 'ciLow' | 'ciHigh'> {
  const nc = control.exposed;
  const nt = variant.exposed;
  if (nc === 0 || nt === 0) return { z: null, pValue: null, ciLow: null, ciHigh: null };
  const pc = control.converted / nc;
  const pt = variant.converted / nt;
  const pooled = (control.converted + variant.converted) / (nc + nt);
  const se = Math.sqrt(pooled * (1 - pooled) * (1 / nc + 1 / nt));
  const z = se === 0 ? null : (pt - pc) / se;
  const pValue = z === null ? null : 2 * (1 - normalCdf(Math.abs(z)));
  const margin = 1.96 * Math.sqrt((pc * (1 - pc)) / nc + (pt * (1 - pt)) / nt);
  return { z, pValue, ciLow: pt - pc - margin, ciHigh: pt - pc + margin };
}

/** Standard normal CDF via the Abramowitz & Stegun 7.1.26 erf approximation (|err| < 1.5e-7). */
function normalCdf(x: number): number {
  return 0.5 * (1 + erf(x / Math.SQRT2));
}

function erf(x: number): number {
  const sign = x < 0 ? -1 : 1;
  const ax = Math.abs(x);
  const t = 1 / (1 + 0.3275911 * ax);
  const y =
    1 -
    ((((1.061405429 * t - 1.453152027) * t + 1.421413741) * t - 0.284496736) * t + 0.254829592) *
      t *
      Math.exp(-ax * ax);
  return sign * y;
}
